package CharBoolean_5;

public class Char_Boolean {
    public static void main(String[] args) {
        
        char a = 'J';
        System.out.println("a = "+a);
        boolean b = true;
        System.out.println("b = "+b);
        char c = '\u00A9';
        System.out.println("c = "+c);
        char d = '\u00AE';
        System.out.println("d = "+d);
    }
    
}
