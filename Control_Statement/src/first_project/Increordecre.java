package first_project;

public class Increordecre {

    public static void main(String[] args) {
        int i = 10, j = 10, k = 10;
        i = ++i;
        i = i--;
        j--;
        System.out.println(i);
        System.out.println(j);
        System.out.println(k++);
        System.out.println(++k);

    }

}
